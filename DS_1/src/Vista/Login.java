    public void registrarse(){
        String cedula = tusuario.getText();
        String usuario = tcontrasena.getText();
        if(tusuario.getText().equals("") || tcontrasena.getText().equals("")){
           JOptionPane.showMessageDialog(rootPane, "TODOS LOS CAMPOS SON OBLIGATORIS");
        }
        else{
          v = vd.Login(cedula, usuario);
          if(!(v.getCedula() != null && v.getUruario() != null)){
               VPrincipal p = new VPrincipal();
               p.setVisible(true);
               dispose();
          } else {
              JOptionPane.showMessageDialog(this, "INGRESE DATOS VALIDOS");
              
            }
        }
    }
